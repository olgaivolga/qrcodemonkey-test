Cypress.Commands.add("check_url_format", (url) => {
    let host = 'api.qrcode-monkey.com'
    var re = new RegExp('^//'+host+'/tmp/'+'[A-Za-z0-9-]{32}\.svg', "g");
    return re.test(url);
})
